def drawBorderLine(a, b):
    length = 40
    vX0 = b[0] - a[0]; vY0 = b[1] - a[1]
    mag = math.sqrt(vX0*vX0 + vY0*vY0)
    vX = vX0 / mag; vY = vY0 / mag
    temp = vX; vX = -vY; vY = temp
    z0 = (int(a[0]+vX0/2), int(a[1]+vY0/2))
    z1 = (int(a[0]+vX0/2 - vX * length), int(a[1] +vY0/2- vY * length))
    return z0, z1

